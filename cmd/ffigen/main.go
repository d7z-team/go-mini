package main

import (
	"flag"
	"fmt"
	"go/ast"
	"go/parser"
	"go/token"
	"os"
	"strings"
)

var (
	pkgName = flag.String("pkg", "", "package name")
	outFile = flag.String("out", "", "output file")
)

func main() {
	flag.Parse()
	if *pkgName == "" || *outFile == "" {
		fmt.Println("Usage: ffigen -pkg <name> -out <file> [input files...]")
		os.Exit(1)
	}

	fset := token.NewFileSet()
	var interfaces []string

	// Simple parse args for files or use current directory
	for _, arg := range flag.Args() {
		node, err := parser.ParseFile(fset, arg, nil, parser.ParseComments)
		if err != nil {
			panic(err)
		}

		ast.Inspect(node, func(n ast.Node) bool {
			if typeSpec, ok := n.(*ast.TypeSpec); ok {
				if iface, ok := typeSpec.Type.(*ast.InterfaceType); ok {
					interfaces = append(interfaces, generateCode(*pkgName, typeSpec.Name.Name, iface))
				}
			}
			return true
		})
	}

	out := fmt.Sprintf("// Code generated by ffigen. DO NOT EDIT.\npackage %s\n\nimport (\n\t\"fmt\"\n\t\"gopkg.d7z.net/go-mini/core/ffigo\"\n)\n\n", *pkgName)
	out += strings.Join(interfaces, "\n")

	err := os.WriteFile(*outFile, []byte(out), 0644)
	if err != nil {
		panic(err)
	}
}

func generateCode(pkg, name string, iface *ast.InterfaceType) string {
	var sb strings.Builder

	// Generate Method IDs
	sb.WriteString(fmt.Sprintf("const (\n"))
	for i, method := range iface.Methods.List {
		if len(method.Names) == 0 {
			continue
		}
		methodName := method.Names[0].Name
		sb.WriteString(fmt.Sprintf("\tMethodID_%s_%s = %d\n", name, methodName, i+1))
	}
	sb.WriteString(")\n\n")

	// Generate VM Proxy
	sb.WriteString(fmt.Sprintf("type %sProxy struct {\n\tbridge ffigo.FFIBridge\n}\n\n", name))
	
	for _, method := range iface.Methods.List {
		if len(method.Names) == 0 {
			continue
		}
		methodName := method.Names[0].Name
		funcType := method.Type.(*ast.FuncType)

		sb.WriteString(fmt.Sprintf("func (p *%sProxy) %s(", name, methodName))

		// Params
		var paramNames []string
		if funcType.Params != nil {
			for j, param := range funcType.Params.List {
				for _, pName := range param.Names {
					pType := typeToString(param.Type)
					sb.WriteString(fmt.Sprintf("%s %s", pName.Name, pType))
					if j < len(funcType.Params.List)-1 {
						sb.WriteString(", ")
					}
					paramNames = append(paramNames, pName.Name)
				}
			}
		}
		sb.WriteString(") ")

		// Results
		var hasErr bool
		if funcType.Results != nil {
			sb.WriteString("(")
			for j, result := range funcType.Results.List {
				rType := typeToString(result.Type)
				if rType == "error" {
					hasErr = true
				}
				sb.WriteString(rType)
				if j < len(funcType.Results.List)-1 {
					sb.WriteString(", ")
				}
			}
			sb.WriteString(") ")
		}

		sb.WriteString("{\n")
		sb.WriteString("\tbuf := ffigo.GetBuffer()\n")
		sb.WriteString("\tdefer ffigo.ReleaseBuffer(buf)\n\n")

		// Serialize args
		if funcType.Params != nil {
			for _, param := range funcType.Params.List {
				for _, pName := range param.Names {
					pType := typeToString(param.Type)
					switch pType {
					case "uint32":
						sb.WriteString(fmt.Sprintf("\tbuf.WriteUint32(%s)\n", pName.Name))
					case "int":
						sb.WriteString(fmt.Sprintf("\tbuf.WriteInt64(int64(%s))\n", pName.Name))
					case "string":
						sb.WriteString(fmt.Sprintf("\tbuf.WriteString(%s)\n", pName.Name))
					case "[]byte":
						sb.WriteString(fmt.Sprintf("\tbuf.WriteBytes(%s)\n", pName.Name))
					}
				}
			}
		}

		sb.WriteString(fmt.Sprintf("\n\tretData, err := p.bridge.Call(MethodID_%s_%s, buf.Bytes())\n", name, methodName))
		if hasErr {
			sb.WriteString("\tif err != nil {\n\t\treturn ")
			// Return zero values + err
			if funcType.Results != nil {
				for _, result := range funcType.Results.List {
					rType := typeToString(result.Type)
					if rType == "error" {
						sb.WriteString("err")
					} else {
						sb.WriteString(zeroValue(rType))
						sb.WriteString(", ")
					}
				}
			}
			sb.WriteString("\n\t}\n\n")
		} else {
			sb.WriteString("\t_ = err\n")
		}

		sb.WriteString("\tretBuf := ffigo.NewReader(retData)\n")
		
		// Deserialize results
		var retStmt []string
		if funcType.Results != nil {
			for _, result := range funcType.Results.List {
				rType := typeToString(result.Type)
				if rType == "error" {
					retStmt = append(retStmt, "nil") // Simplified error handling
					continue
				}
				switch rType {
				case "uint32":
					sb.WriteString(fmt.Sprintf("\tv_%s := retBuf.ReadUint32()\n", rType))
					retStmt = append(retStmt, fmt.Sprintf("v_%s", rType))
				case "int":
					sb.WriteString(fmt.Sprintf("\tv_%s := int(retBuf.ReadInt64())\n", rType))
					retStmt = append(retStmt, fmt.Sprintf("v_%s", rType))
				case "string":
					sb.WriteString(fmt.Sprintf("\tv_%s := retBuf.ReadString()\n", rType))
					retStmt = append(retStmt, fmt.Sprintf("v_%s", rType))
				case "[]byte":
					sb.WriteString(fmt.Sprintf("\tv_bytes := retBuf.ReadBytes()\n"))
					retStmt = append(retStmt, "v_bytes")
				}
			}
		}
		
		if len(retStmt) > 0 {
			sb.WriteString(fmt.Sprintf("\treturn %s\n", strings.Join(retStmt, ", ")))
		}

		sb.WriteString("}\n\n")
	}

	// Generate Host Router
	sb.WriteString(fmt.Sprintf("func %sHostRouter(impl %s, methodID uint32, args []byte) ([]byte, error) {\n", name, name))
	sb.WriteString("\treqBuf := ffigo.NewReader(args)\n")
	sb.WriteString("\tswitch methodID {\n")

	for _, method := range iface.Methods.List {
		if len(method.Names) == 0 {
			continue
		}
		methodName := method.Names[0].Name
		funcType := method.Type.(*ast.FuncType)

		sb.WriteString(fmt.Sprintf("\tcase MethodID_%s_%s:\n", name, methodName))

		var paramVars []string
		if funcType.Params != nil {
			for _, param := range funcType.Params.List {
				for _, pName := range param.Names {
					pType := typeToString(param.Type)
					switch pType {
					case "uint32":
						sb.WriteString(fmt.Sprintf("\t\t%s := reqBuf.ReadUint32()\n", pName.Name))
					case "int":
						sb.WriteString(fmt.Sprintf("\t\t%s := int(reqBuf.ReadInt64())\n", pName.Name))
					case "string":
						sb.WriteString(fmt.Sprintf("\t\t%s := reqBuf.ReadString()\n", pName.Name))
					case "[]byte":
						sb.WriteString(fmt.Sprintf("\t\t%s := reqBuf.ReadBytes()\n", pName.Name))
					}
					paramVars = append(paramVars, pName.Name)
				}
			}
		}

		callStmt := fmt.Sprintf("impl.%s(%s)", methodName, strings.Join(paramVars, ", "))
		
		var retVars []string
		var hasErr bool
		if funcType.Results != nil {
			for i, result := range funcType.Results.List {
				rType := typeToString(result.Type)
				if rType == "error" {
					hasErr = true
					retVars = append(retVars, "err")
				} else {
					retVars = append(retVars, fmt.Sprintf("r%d", i))
				}
			}
			sb.WriteString(fmt.Sprintf("\t\t%s := %s\n", strings.Join(retVars, ", "), callStmt))
		} else {
			sb.WriteString(fmt.Sprintf("\t\t%s\n", callStmt))
		}

		sb.WriteString("\t\tresBuf := ffigo.GetBuffer()\n")
		
		if funcType.Results != nil {
			for i, result := range funcType.Results.List {
				rType := typeToString(result.Type)
				if rType == "error" { continue }
				
				switch rType {
				case "uint32":
					sb.WriteString(fmt.Sprintf("\t\tresBuf.WriteUint32(r%d)\n", i))
				case "int":
					sb.WriteString(fmt.Sprintf("\t\tresBuf.WriteInt64(int64(r%d))\n", i))
				case "string":
					sb.WriteString(fmt.Sprintf("\t\tresBuf.WriteString(r%d)\n", i))
				case "[]byte":
					sb.WriteString(fmt.Sprintf("\t\tresBuf.WriteBytes(r%d)\n", i))
				}
			}
		}
		
		if hasErr {
			sb.WriteString("\t\treturn resBuf.Bytes(), err\n")
		} else {
			sb.WriteString("\t\treturn resBuf.Bytes(), nil\n")
		}
	}

	sb.WriteString("\tdefault:\n\t\treturn nil, fmt.Errorf(\"unknown method ID %%d\", methodID)\n")
	sb.WriteString("\t}\n}\n")

	return sb.String()
}

func typeToString(expr ast.Expr) string {
	switch t := expr.(type) {
	case *ast.Ident:
		return t.Name
	case *ast.ArrayType:
		return "[]" + typeToString(t.Elt)
	default:
		return "unknown"
	}
}

func zeroValue(t string) string {
	switch t {
	case "uint32", "int":
		return "0"
	case "string":
		return "\"\""
	case "[]byte":
		return "nil"
	default:
		return "nil"
	}
}
