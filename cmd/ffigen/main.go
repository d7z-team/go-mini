package main

import (
	"bytes"
	"flag"
	"fmt"
	"go/ast"
	"go/format"
	"go/parser"
	"go/token"
	"os"
	"sort"
	"strings"
)

var (
	pkgName = flag.String("pkg", "", "package name")
	outFile = flag.String("out", "", "output file")
)

func main() {
	flag.Parse()
	if *pkgName == "" || *outFile == "" {
		fmt.Println("Usage: ffigen -pkg <name> -out <file> [input files...]")
		os.Exit(1)
	}

	fset := token.NewFileSet()
	var ifaceSpecs []*ast.TypeSpec
	structs := make(map[string]*ast.StructType)

	for _, arg := range flag.Args() {
		node, err := parser.ParseFile(fset, arg, nil, parser.ParseComments)
		if err != nil {
			panic(err)
		}

		ast.Inspect(node, func(n ast.Node) bool {
			if gd, ok := n.(*ast.GenDecl); ok {
				for _, spec := range gd.Specs {
					if typeSpec, ok := spec.(*ast.TypeSpec); ok {
						if _, ok := typeSpec.Type.(*ast.InterfaceType); ok {
							// 关联文档：优先使用 typeSpec.Doc，其次使用 gd.Doc (针对 type X interface {})
							if typeSpec.Doc == nil {
								typeSpec.Doc = gd.Doc
							}
							ifaceSpecs = append(ifaceSpecs, typeSpec)
						} else if str, ok := typeSpec.Type.(*ast.StructType); ok {
							structs[typeSpec.Name.Name] = str
						}
					}
				}
			}
			return true
		})
	}

	var interfaces []string
	anyReverse := false
	for _, spec := range ifaceSpecs {
		isReverse := false
		if spec.Doc != nil {
			for _, line := range spec.Doc.List {
				if strings.Contains(line.Text, "ffigen:reverse") {
					isReverse = true
					anyReverse = true
					break
				}
			}
		}
		interfaces = append(interfaces, generateCode(*pkgName, spec, structs, isReverse))
	}

	var sb strings.Builder
	sb.WriteString("// Code generated by ffigen. DO NOT EDIT.\n")
	fmt.Fprintf(&sb, "package %s\n\n", *pkgName)
	sb.WriteString("import (\n")
	sb.WriteString("\t\"context\"\n")
	sb.WriteString("\t\"fmt\"\n")
	sb.WriteString("\t\"strings\"\n")
	sb.WriteString("\t\"gopkg.d7z.net/go-mini/core/ffigo\"\n")
	sb.WriteString("\t\"gopkg.d7z.net/go-mini/core/ast\"\n")
	if anyReverse {
		sb.WriteString("\t\"gopkg.d7z.net/go-mini/core/runtime\"\n")
	}
	sb.WriteString(")\n\n")
	sb.WriteString(strings.Join(interfaces, "\n"))

	src := []byte(sb.String())

	f, err := parser.ParseFile(fset, "", src, parser.ParseComments)
	if err != nil {
		fmt.Fprintf(os.Stderr, "Error: generated code has syntax errors: %v\n", err)
		_ = os.WriteFile(*outFile, src, 0o644)
		os.Exit(1)
	}

	var buf bytes.Buffer
	if err := format.Node(&buf, fset, f); err != nil {
		fmt.Fprintf(os.Stderr, "Error: failed to format generated code: %v\n", err)
		_ = os.WriteFile(*outFile, src, 0o644)
		os.Exit(1)
	}

	err = os.WriteFile(*outFile, buf.Bytes(), 0o644)
	if err != nil {
		panic(err)
	}
}

func readArrayItemType(pType string) (string, bool) {
	if strings.HasPrefix(pType, "Array<") && strings.HasSuffix(pType, ">") {
		return pType[6 : len(pType)-1], true
	}
	return "", false
}

func readMapKeyValueTypes(pType string) (string, string, bool) {
	if strings.HasPrefix(pType, "Map<") && strings.HasSuffix(pType, ">") {
		inner := pType[4 : len(pType)-1]
		parts := strings.SplitN(inner, ",", 2)
		if len(parts) == 2 {
			return strings.TrimSpace(parts[0]), strings.TrimSpace(parts[1]), true
		}
	}
	return "", "", false
}

func emitWrite(sb *strings.Builder, prefix, pType string, structs map[string]*ast.StructType, bufName string, isHost bool) {
	if strings.HasPrefix(pType, "Ptr<") {
		if isHost {
			fmt.Fprintf(sb, "\t\t%s.WriteUint32(registry.Register(%s))\n", bufName, prefix)
		} else {
			fmt.Fprintf(sb, "\t\tif p.registry != nil {\n")
			fmt.Fprintf(sb, "\t\t\t%s.WriteUint32(p.registry.Register(%s))\n", bufName, prefix)
			fmt.Fprintf(sb, "\t\t} else {\n")
			fmt.Fprintf(sb, "\t\t\t%s.WriteUint32(0)\n", bufName)
			fmt.Fprintf(sb, "\t\t}\n")
		}
		return
	}
	if strings.HasPrefix(pType, "interface{") {
		// 接口序列化：Handle + 方法列表
		fmt.Fprintf(sb, "\t\tmethods := make(map[string]string)\n")
		fmt.Fprintf(sb, "\t\t// TODO: 获取 Go 接口方法集并填充 methods (核心反向代理支持)\n")
		if isHost {
			fmt.Fprintf(sb, "\t\t%s.WriteInterface(registry.Register(%s), methods)\n", bufName, prefix)
		} else {
			fmt.Fprintf(sb, "\t\tif p.registry != nil {\n")
			fmt.Fprintf(sb, "\t\t\t%s.WriteInterface(p.registry.Register(%s), methods)\n", bufName, prefix)
			fmt.Fprintf(sb, "\t\t} else {\n")
			fmt.Fprintf(sb, "\t\t\t%s.WriteInterface(0, nil)\n", bufName)
			fmt.Fprintf(sb, "\t\t}\n")
		}
		return
	}
	switch pType {
	case "Uint32", "uint32", "Int32", "int32":
		fmt.Fprintf(sb, "\t%s.WriteUint32(uint32(%s))\n", bufName, prefix)
	case "int", "Int", "Int64", "int64":
		fmt.Fprintf(sb, "\t%s.WriteInt64(int64(%s))\n", bufName, prefix)
	case "string", "String":
		fmt.Fprintf(sb, "\t%s.WriteString(%s)\n", bufName, prefix)
	case "[]byte", "TypeBytes", "Array<Uint8>", "Array<byte>":
		fmt.Fprintf(sb, "\t%s.WriteBytes(%s)\n", bufName, prefix)
	case "Any", "any":
		fmt.Fprintf(sb, "\t%s.WriteAny(%s)\n", bufName, prefix)
	case "bool", "Bool":
		fmt.Fprintf(sb, "\t%s.WriteBool(%s)\n", bufName, prefix)
	case "float64", "Float64":
		fmt.Fprintf(sb, "\t%s.WriteFloat64(float64(%s))\n", bufName, prefix)
	default:
		if itemType, ok := readArrayItemType(pType); ok {
			fmt.Fprintf(sb, "\t%s.WriteUint32(uint32(len(%s)))\n", bufName, prefix)
			fmt.Fprintf(sb, "\tfor _, item := range %s {\n", prefix)
			emitWrite(sb, "item", itemType, structs, bufName, isHost)
			fmt.Fprintf(sb, "\t}\n")
			return
		}
		if kType, vType, ok := readMapKeyValueTypes(pType); ok {
			fmt.Fprintf(sb, "\t%s.WriteUint32(uint32(len(%s)))\n", bufName, prefix)
			fmt.Fprintf(sb, "\tfor k, v := range %s {\n", prefix)
			emitWrite(sb, "k", kType, structs, bufName, isHost)
			emitWrite(sb, "v", vType, structs, bufName, isHost)
			fmt.Fprintf(sb, "\t}\n")
			return
		}
		if str, ok := structs[pType]; ok {
			var fieldNames []string
			fieldMap := make(map[string]ast.Expr)
			for _, field := range str.Fields.List {
				for _, fName := range field.Names {
					fieldNames = append(fieldNames, fName.Name)
					fieldMap[fName.Name] = field.Type
				}
			}
			sort.Strings(fieldNames)
			for _, fName := range fieldNames {
				fType := typeToString(fieldMap[fName])
				emitWrite(sb, prefix+"."+fName, fType, structs, bufName, isHost)
			}
		} else {
			fmt.Fprintf(sb, "\t// Unknown type for write: %s\n", pType)
		}
	}
}

func emitReadAssign(sb *strings.Builder, varName, pType string, structs map[string]*ast.StructType, readerName string, isHost bool) {
	if strings.HasPrefix(pType, "Ptr<") {
		if isHost {
			fmt.Fprintf(sb, "\t\tif id := %s.ReadUint32(); id != 0 {\n", readerName)
			fmt.Fprintf(sb, "\t\t\tif obj, ok := registry.Get(id); ok {\n")
			fmt.Fprintf(sb, "\t\t\t\t%s = obj.(%s)\n", varName, toGoType(pType))
			fmt.Fprintf(sb, "\t\t\t} else {\n")
			fmt.Fprintf(sb, "\t\t\t\treturn nil, fmt.Errorf(\"invalid handle ID: %%d\", id)\n")
			fmt.Fprintf(sb, "\t\t\t}\n")
			fmt.Fprintf(sb, "\t\t}\n")
		} else {
			fmt.Fprintf(sb, "\t\tif id := %s.ReadUint32(); id != 0 {\n", readerName)
			fmt.Fprintf(sb, "\t\t\tif p.registry != nil {\n")
			fmt.Fprintf(sb, "\t\t\t\tif obj, ok := p.registry.Get(id); ok {\n")
			fmt.Fprintf(sb, "\t\t\t\t\t%s = obj.(%s)\n", varName, toGoType(pType))
			fmt.Fprintf(sb, "\t\t\t\t}\n")
			fmt.Fprintf(sb, "\t\t\t}\n")
			fmt.Fprintf(sb, "\t\t}\n")
		}
		return
	}
	if strings.HasPrefix(pType, "interface{") {
		// 接口反序列化：Handle + 方法列表
		fmt.Fprintf(sb, "\t\tif h, m := %s.ReadInterface(); h != 0 {\n", readerName)
		fmt.Fprintf(sb, "\t\t\t// TODO: 创建脚本到 Go 的反向代理 (Reverse Proxy)\n")
		fmt.Fprintf(sb, "\t\t\t_ = h\n")
		fmt.Fprintf(sb, "\t\t\t_ = m\n")
		fmt.Fprintf(sb, "\t\t}\n")
		return
	}
	switch pType {
	case "Uint32", "uint32", "Int32", "int32":
		fmt.Fprintf(sb, "\t%s = %s(%s.ReadUint32())\n", varName, toGoType(pType), readerName)
	case "int", "Int", "Int64", "int64":
		fmt.Fprintf(sb, "\t%s = %s(%s.ReadInt64())\n", varName, toGoType(pType), readerName)
	case "Uint8", "byte", "uint8":
		fmt.Fprintf(sb, "\t%s = %s(%s.ReadInt64())\n", varName, toGoType(pType), readerName)
	case "string", "String":
		fmt.Fprintf(sb, "\t%s = %s.ReadString()\n", varName, readerName)
	case "[]byte", "TypeBytes", "Array<Uint8>", "Array<byte>":
		fmt.Fprintf(sb, "\t%s = %s.ReadBytes()\n", varName, readerName)
	case "bool", "Bool":
		fmt.Fprintf(sb, "\t%s = %s.ReadBool()\n", varName, readerName)
	case "float64", "Float64":
		fmt.Fprintf(sb, "\t%s = %s.ReadFloat64()\n", varName, readerName)
	case "Any", "any":
		if isHost {
			fmt.Fprintf(sb, "\t\trawVal := %s.ReadAny()\n", readerName)
			fmt.Fprintf(sb, "\t\tswitch rv := rawVal.(type) {\n")
			fmt.Fprintf(sb, "\t\tcase uint32:\n")
			fmt.Fprintf(sb, "\t\t\tif obj, ok := registry.Get(rv); ok { %s = obj } else { %s = rv }\n", varName, varName)
			fmt.Fprintf(sb, "\t\tcase ffigo.ErrorData:\n")
			fmt.Fprintf(sb, "\t\t\tif rv.Handle != 0 {\n")
			fmt.Fprintf(sb, "\t\t\t\tif obj, ok := registry.Get(rv.Handle); ok { %s = obj } else { %s = rv }\n", varName, varName)
			fmt.Fprintf(sb, "\t\t\t} else { %s = rv }\n", varName)
			fmt.Fprintf(sb, "\t\tdefault: %s = rawVal\n", varName)
			fmt.Fprintf(sb, "\t\t}\n")
		} else {
			fmt.Fprintf(sb, "\t%s = %s.ReadAny()\n", varName, readerName)
		}
	default:
		if itemType, ok := readArrayItemType(pType); ok {
			fmt.Fprintf(sb, "\tl_%s := int(%s.ReadUint32())\n", varName, readerName)
			fmt.Fprintf(sb, "\t%s = make(%s, l_%s)\n", varName, toGoType(pType), varName)
			fmt.Fprintf(sb, "\tfor i_%s := 0; i_%s < l_%s; i_%s++ {\n", varName, varName, varName, varName)
			emitReadAssign(sb, fmt.Sprintf("%s[i_%s]", varName, varName), itemType, structs, readerName, isHost)
			fmt.Fprintf(sb, "\t}\n")
			return
		}
		if kType, vType, ok := readMapKeyValueTypes(pType); ok {
			fmt.Fprintf(sb, "\tl_%s := int(%s.ReadUint32())\n", varName, readerName)
			fmt.Fprintf(sb, "\t%s = make(%s)\n", varName, toGoType(pType))
			fmt.Fprintf(sb, "\tfor i_%s := 0; i_%s < l_%s; i_%s++ {\n", varName, varName, varName, varName)
			fmt.Fprintf(sb, "\t\tvar k %s\n", toGoType(kType))
			fmt.Fprintf(sb, "\t\tvar v %s\n", toGoType(vType))
			emitReadAssign(sb, "k", kType, structs, readerName, isHost)
			emitReadAssign(sb, "v", vType, structs, readerName, isHost)
			fmt.Fprintf(sb, "\t\t%s[k] = v\n", varName)
			fmt.Fprintf(sb, "\t}\n")
			return
		}
		if str, ok := structs[pType]; ok {
			var fieldNames []string
			fieldMap := make(map[string]ast.Expr)
			for _, field := range str.Fields.List {
				for _, fName := range field.Names {
					fieldNames = append(fieldNames, fName.Name)
					fieldMap[fName.Name] = field.Type
				}
			}
			sort.Strings(fieldNames)
			for _, fName := range fieldNames {
				fType := typeToString(fieldMap[fName])
				emitReadAssign(sb, varName+"."+fName, fType, structs, readerName, isHost)
			}
		} else {
			fmt.Fprintf(sb, "\t// Unknown type for read: %s\n", pType)
		}
	}
}

func toGoType(pType string) string {
	if pType == "Any" || pType == "any" {
		return "any"
	}
	if strings.HasPrefix(pType, "Array<") && strings.HasSuffix(pType, ">") {
		return "[]" + toGoType(pType[6:len(pType)-1])
	}
	if strings.HasPrefix(pType, "Map<") && strings.HasSuffix(pType, ">") {
		kType, vType, _ := readMapKeyValueTypes(pType)
		return "map[" + toGoType(kType) + "]" + toGoType(vType)
	}
	if strings.HasPrefix(pType, "Ptr<") && strings.HasSuffix(pType, ">") {
		return "*" + toGoType(pType[4:len(pType)-1])
	}
	switch pType {
	case "Int64", "int64":
		return "int64"
	case "Int", "int":
		return "int"
	case "Int32", "int32":
		return "int32"
	case "Uint32", "uint32":
		return "uint32"
	case "Uint8", "byte", "uint8":
		return "byte"
	case "Float64", "float64":
		return "float64"
	case "String", "string":
		return "string"
	case "Bool", "bool":
		return "bool"
	case "TypeBytes":
		return "[]byte"
	}
	return pType
}

func generateCode(pkg string, spec *ast.TypeSpec, structs map[string]*ast.StructType, isReverse bool) string {
	name := spec.Name.Name
	iface := spec.Type.(*ast.InterfaceType)
	var sb strings.Builder

	// 解析模块名和前缀
	fixedPrefix := ""
	if spec.Doc != nil {
		for _, comment := range spec.Doc.List {
			text := strings.TrimSpace(strings.TrimPrefix(comment.Text, "//"))
			if strings.HasPrefix(text, "ffigen:module ") {
				fixedPrefix = strings.TrimSpace(strings.TrimPrefix(text, "ffigen:module "))
			} else if strings.HasPrefix(text, "ffigen:methods ") {
				typeName := strings.TrimSpace(strings.TrimPrefix(text, "ffigen:methods "))
				fixedPrefix = "__method_" + typeName
			}
		}
	}

	// 1. 生成基础 ID 和常规 Proxy/Router
	fmt.Fprintf(&sb, "const (\n")
	// ... (保持原有 MethodID 生成) ...
	for i, method := range iface.Methods.List {
		if len(method.Names) == 0 {
			continue
		}
		fmt.Fprintf(&sb, "\tMethodID_%s_%s = %d\n", name, method.Names[0].Name, i+1)
	}
	fmt.Fprintf(&sb, ")\n\n")

	fmt.Fprintf(&sb, "type %sProxy struct {\n\tbridge ffigo.FFIBridge\n\tregistry *ffigo.HandleRegistry\n}\n\n", name)

	for _, method := range iface.Methods.List {
		if len(method.Names) == 0 {
			continue
		}
		methodName := method.Names[0].Name
		funcType := method.Type.(*ast.FuncType)

		hasContext := false
		if funcType.Params != nil && len(funcType.Params.List) > 0 {
			pType := typeToString(funcType.Params.List[0].Type)
			if pType == "Context" || strings.HasSuffix(pType, ".Context") {
				hasContext = true
			}
		}

		fmt.Fprintf(&sb, "func (p *%sProxy) %s(ctx context.Context, ", name, methodName)
		if funcType.Params != nil {
			for j, param := range funcType.Params.List {
				if j == 0 && hasContext {
					continue
				}
				for k, pName := range param.Names {
					goType := toGoType(typeToString(param.Type))
					if _, ok := param.Type.(*ast.Ellipsis); ok {
						goType = "..." + strings.TrimPrefix(goType, "[]")
					}
					fmt.Fprintf(&sb, "%s %s", pName.Name, goType)
					if j < len(funcType.Params.List)-1 || k < len(param.Names)-1 {
						fmt.Fprintf(&sb, ", ")
					}
				}
			}
		}
		fmt.Fprintf(&sb, ") ")

		var hasErr bool
		if funcType.Results != nil {
			fmt.Fprintf(&sb, "(")
			for j, result := range funcType.Results.List {
				rType := typeToString(result.Type)
				if rType == "error" {
					hasErr = true
					fmt.Fprintf(&sb, "error")
				} else {
					fmt.Fprintf(&sb, "%s", toGoType(rType))
				}
				if j < len(funcType.Results.List)-1 {
					fmt.Fprintf(&sb, ", ")
				}
			}
			fmt.Fprintf(&sb, ") ")
		}

		fmt.Fprintf(&sb, "{\n\tbuf := ffigo.GetBuffer()\n\tdefer ffigo.ReleaseBuffer(buf)\n\n")
		if funcType.Params != nil {
			for j, param := range funcType.Params.List {
				if j == 0 && hasContext {
					continue
				}
				for _, pName := range param.Names {
					pType := typeToString(param.Type)
					if _, ok := param.Type.(*ast.Ellipsis); ok {
						itemType, _ := readArrayItemType(pType)
						fmt.Fprintf(&sb, "\tbuf.WriteUint32(uint32(len(%s)))\n", pName.Name)
						fmt.Fprintf(&sb, "\tfor _, item := range %s {\n", pName.Name)
						emitWrite(&sb, "item", itemType, structs, "buf", false)
						fmt.Fprintf(&sb, "\t}\n")
					} else {
						emitWrite(&sb, pName.Name, pType, structs, "buf", false)
					}
				}
			}
		}

		needsRetBuf := false
		if funcType.Results != nil && len(funcType.Results.List) > 0 {
			needsRetBuf = true
		}

		if needsRetBuf || hasErr {
			fmt.Fprintf(&sb, "\n\tretData, err := p.bridge.Call(ctx, MethodID_%s_%s, buf.Bytes())\n", name, methodName)
			fmt.Fprintf(&sb, "\t_ = retData\n")
		} else {
			fmt.Fprintf(&sb, "\n\t_, err := p.bridge.Call(ctx, MethodID_%s_%s, buf.Bytes())\n", name, methodName)
		}
		fmt.Fprintf(&sb, "\t_ = err\n")

		if hasErr {
			fmt.Fprintf(&sb, "\tif err != nil { return ")
			if funcType.Results != nil {
				for j, result := range funcType.Results.List {
					rType := typeToString(result.Type)
					if rType == "error" {
						fmt.Fprintf(&sb, "err")
					} else {
						fmt.Fprintf(&sb, "%s", zeroValue(toGoType(rType)))
					}
					if j < len(funcType.Results.List)-1 {
						fmt.Fprintf(&sb, ", ")
					}
				}
			}
			fmt.Fprintf(&sb, " }\n")
		}

		if needsRetBuf {
			fmt.Fprintf(&sb, "\tretBuf := ffigo.NewReader(retData)\n")
		}

		var retStmt []string
		if funcType.Results != nil {
			for i, result := range funcType.Results.List {
				rType := typeToString(result.Type)
				if rType == "error" {
					fmt.Fprintf(&sb, "\tvar err_%d error\n", i)
					fmt.Fprintf(&sb, "\tif rawErr := retBuf.ReadAny(); rawErr != nil {\n")
					fmt.Fprintf(&sb, "\t\tif ed, ok := rawErr.(ffigo.ErrorData); ok {\n")
					fmt.Fprintf(&sb, "\t\t\tif ed.Handle != 0 && p.registry != nil {\n")
					fmt.Fprintf(&sb, "\t\t\t\tif obj, ok := p.registry.Get(ed.Handle); ok {\n")
					fmt.Fprintf(&sb, "\t\t\t\t\terr_%d = obj.(error)\n", i)
					fmt.Fprintf(&sb, "\t\t\t\t} else { err_%d = fmt.Errorf(\"%%s\", ed.Message) }\n", i)
					fmt.Fprintf(&sb, "\t\t\t} else { err_%d = fmt.Errorf(\"%%s\", ed.Message) }\n", i)
					fmt.Fprintf(&sb, "\t\t} else if s, ok := rawErr.(string); ok && s != \"\" {\n")
					fmt.Fprintf(&sb, "\t\t\terr_%d = fmt.Errorf(\"%%s\", s)\n", i)
					fmt.Fprintf(&sb, "\t\t}\n\t}\n")
					retStmt = append(retStmt, fmt.Sprintf("err_%d", i))
					continue
				}
				varName := fmt.Sprintf("v_%d", i)
				fmt.Fprintf(&sb, "\tvar %s %s\n", varName, toGoType(rType))
				emitReadAssign(&sb, varName, rType, structs, "retBuf", false)
				retStmt = append(retStmt, varName)
			}
		}
		if len(retStmt) > 0 {
			fmt.Fprintf(&sb, "\treturn %s\n", strings.Join(retStmt, ", "))
		} else {
			fmt.Fprintf(&sb, "\treturn\n")
		}
		fmt.Fprintf(&sb, "}\n\n")
	}

	fmt.Fprintf(&sb, "func %sHostRouter(ctx context.Context, impl %s, registry *ffigo.HandleRegistry, methodID uint32, methodName string, args []byte) ([]byte, error) {\n", name, name)
	fmt.Fprintf(&sb, "\tif methodID == 0 && methodName != \"\" {\n")
	fmt.Fprintf(&sb, "\t\tswitch methodName {\n")
	for _, method := range iface.Methods.List {
		if len(method.Names) == 0 {
			continue
		}
		methodName := method.Names[0].Name
		fmt.Fprintf(&sb, "\t\tcase \"%s\":\n", methodName)
		fmt.Fprintf(&sb, "\t\t\tmethodID = MethodID_%s_%s\n", name, methodName)
	}
	fmt.Fprintf(&sb, "\t\t}\n")
	fmt.Fprintf(&sb, "\t}\n\n")

	fmt.Fprintf(&sb, "\treqBuf := ffigo.NewReader(args)\n\tswitch methodID {\n")
	for _, method := range iface.Methods.List {
		if len(method.Names) == 0 {
			continue
		}
		methodName := method.Names[0].Name
		funcType := method.Type.(*ast.FuncType)
		hasContext := false
		if funcType.Params != nil && len(funcType.Params.List) > 0 {
			pType := typeToString(funcType.Params.List[0].Type)
			if pType == "Context" || strings.HasSuffix(pType, ".Context") {
				hasContext = true
			}
		}

		fmt.Fprintf(&sb, "\tcase MethodID_%s_%s:\n", name, methodName)
		var paramVars []string
		if hasContext {
			paramVars = append(paramVars, "ctx")
		}
		if funcType.Params != nil {
			for j, param := range funcType.Params.List {
				if j == 0 && hasContext {
					continue
				}
				for _, pName := range param.Names {
					pType := typeToString(param.Type)
					goType := toGoType(pType)
					isVariadic := false
					if _, ok := param.Type.(*ast.Ellipsis); ok {
						isVariadic = true
						goType = "[]" + strings.TrimPrefix(goType, "[]")
					}
					fmt.Fprintf(&sb, "\t\tvar %s %s\n", pName.Name, goType)
					emitReadAssign(&sb, pName.Name, pType, structs, "reqBuf", true)
					if isVariadic {
						paramVars = append(paramVars, pName.Name+"...")
					} else {
						paramVars = append(paramVars, pName.Name)
					}
				}
			}
		}
		var retVars []string
		if funcType.Results != nil {
			for i, result := range funcType.Results.List {
				if typeToString(result.Type) == "error" {
					retVars = append(retVars, "err")
				} else {
					retVars = append(retVars, fmt.Sprintf("r%d", i))
				}
			}
			fmt.Fprintf(&sb, "\t\t%s := impl.%s(%s)\n", strings.Join(retVars, ", "), methodName, strings.Join(paramVars, ", "))
		} else {
			fmt.Fprintf(&sb, "\t\timpl.%s(%s)\n", methodName, strings.Join(paramVars, ", "))
		}
		fmt.Fprintf(&sb, "\t\tresBuf := ffigo.GetBuffer()\n")
		if funcType.Results != nil {
			for i, result := range funcType.Results.List {
				if typeToString(result.Type) == "error" {
					fmt.Fprintf(&sb, "\t\tif err != nil {\n")
					fmt.Fprintf(&sb, "\t\t\tif registry != nil {\n")
					fmt.Fprintf(&sb, "\t\t\t\tresBuf.WriteError(err.Error(), registry.Register(err))\n")
					fmt.Fprintf(&sb, "\t\t\t} else {\n")
					fmt.Fprintf(&sb, "\t\t\t\tresBuf.WriteError(err.Error(), 0)\n")
					fmt.Fprintf(&sb, "\t\t\t}\n")
					fmt.Fprintf(&sb, "\t\t} else {\n")
					fmt.Fprintf(&sb, "\t\t\tresBuf.WriteByte(ffigo.TypeTagUnknown)\n")
					fmt.Fprintf(&sb, "\t\t}\n")
				} else {
					emitWrite(&sb, fmt.Sprintf("r%d", i), typeToString(result.Type), structs, "resBuf", true)
				}
			}
		}
		fmt.Fprintf(&sb, "\t\treturn resBuf.Bytes(), nil\n")
	}
	fmt.Fprintf(&sb, "\tdefault:\n\t\treturn nil, fmt.Errorf(\"unknown method ID %%d\", methodID)\n\t}\n}\n")

	fmt.Fprintf(&sb, "var %s_FFI_Metadata = []struct {\n\tName     string\n\tMethodID uint32\n\tSpec     string\n\tDoc      string\n}{\n", name)
	for i, method := range iface.Methods.List {
		if len(method.Names) == 0 {
			continue
		}
		doc := ""
		if method.Doc != nil {
			doc = strings.ReplaceAll(method.Doc.Text(), "\"", "\\\"")
			doc = strings.ReplaceAll(doc, "\n", " ")
			doc = strings.TrimSpace(doc)
		}
		fmt.Fprintf(&sb, "\t{\"%s\", %d, \"%s\", \"%s\"},\n", method.Names[0].Name, i+1, getSpec(method.Type.(*ast.FuncType)), doc)
	}
	fmt.Fprintf(&sb, "}\n\n")

	fmt.Fprintf(&sb, "type %s_Bridge struct {\n\tImpl %s\n\tRegistry *ffigo.HandleRegistry\n}\n\n", name, name)
	fmt.Fprintf(&sb, "func (b *%s_Bridge) Call(ctx context.Context, methodID uint32, args []byte) ([]byte, error) {\n", name)
	fmt.Fprintf(&sb, "\treturn %sHostRouter(ctx, b.Impl, b.Registry, methodID, \"\", args)\n}\n\n", name)
	fmt.Fprintf(&sb, "func (b *%s_Bridge) Invoke(ctx context.Context, method string, args []byte) ([]byte, error) {\n", name)
	fmt.Fprintf(&sb, "\treturn %sHostRouter(ctx, b.Impl, b.Registry, 0, method, args)\n}\n\n", name)
	fmt.Fprintf(&sb, "func (b *%s_Bridge) DestroyHandle(handle uint32) error {\n\tif b.Registry != nil { b.Registry.Remove(handle) }\n\treturn nil\n}\n\n", name)

	if fixedPrefix != "" {
		fmt.Fprintf(&sb, "func Register%s(executor interface{ RegisterFFI(string, ffigo.FFIBridge, uint32, ast.GoMiniType, string) }, impl %s, registry *ffigo.HandleRegistry) {\n", name, name)
		fmt.Fprintf(&sb, "\tbridge := &%s_Bridge{Impl: impl, Registry: registry}\n", name)
		fmt.Fprintf(&sb, "\tprefix := \"%s\"\n", fixedPrefix)
		fmt.Fprintf(&sb, "\tsep := \".\"\n\tif strings.HasPrefix(prefix, \"__method_\") { sep = \"_\" }\n")
		fmt.Fprintf(&sb, "\tfor _, m := range %s_FFI_Metadata {\n", name)
		fmt.Fprintf(&sb, "\t\texecutor.RegisterFFI(prefix+sep+m.Name, bridge, m.MethodID, ast.GoMiniType(m.Spec), m.Doc)\n\t}\n}\n")
	} else {
		fmt.Fprintf(&sb, "func Register%s%sLibrary(executor interface{ RegisterFFI(string, ffigo.FFIBridge, uint32, ast.GoMiniType, string) }, prefix string, impl %s, registry *ffigo.HandleRegistry) {\n", strings.ToUpper(pkg), name, name)
		fmt.Fprintf(&sb, "\tbridge := &%s_Bridge{Impl: impl, Registry: registry}\n", name)
		fmt.Fprintf(&sb, "\tsep := \".\"\n\tif strings.HasPrefix(prefix, \"__method_\") { sep = \"_\" }\n")
		fmt.Fprintf(&sb, "\tfor _, m := range %s_FFI_Metadata {\n", name)
		fmt.Fprintf(&sb, "\t\texecutor.RegisterFFI(prefix+sep+m.Name, bridge, m.MethodID, ast.GoMiniType(m.Spec), m.Doc)\n\t}\n}\n")
	}

	// 2. 生成 Reverse Proxy (Script -> Go)
	if isReverse {
		fmt.Fprintf(&sb, "type %s_ReverseProxy struct {\n", name)
		fmt.Fprintf(&sb, "\tprogram  runtime.ExecutorAPI\n")
		fmt.Fprintf(&sb, "\tctx      *runtime.StackContext\n")
		fmt.Fprintf(&sb, "\tcallable *runtime.Var\n")
		fmt.Fprintf(&sb, "\tbridge   ffigo.FFIBridge\n")
		fmt.Fprintf(&sb, "}\n\n")

		fmt.Fprintf(&sb, "func New%s_ReverseProxy(program runtime.ExecutorAPI, ctx *runtime.StackContext, callable *runtime.Var, bridge ffigo.FFIBridge) *%s_ReverseProxy {\n", name, name)
		fmt.Fprintf(&sb, "\treturn &%s_ReverseProxy{program: program, ctx: ctx, callable: callable, bridge: bridge}\n}\n\n", name)

		for _, method := range iface.Methods.List {
			if len(method.Names) == 0 {
				continue
			}
			methodName := method.Names[0].Name
			funcType := method.Type.(*ast.FuncType)

			// 生成方法签名
			var paramsList []string
			var paramNames []string
			if funcType.Params != nil {
				for j, p := range funcType.Params.List {
					pType := toGoType(typeToString(p.Type))
					if j == 0 && (pType == "context.Context" || strings.HasSuffix(pType, ".Context")) {
						paramsList = append(paramsList, "ctx "+pType)
						continue
					}
					for _, pn := range p.Names {
						paramsList = append(paramsList, pn.Name+" "+pType)
						paramNames = append(paramNames, pn.Name)
					}
				}
			}
			retType := " "
			if funcType.Results != nil {
				var results []string
				for _, r := range funcType.Results.List {
					results = append(results, toGoType(typeToString(r.Type)))
				}
				if len(results) > 1 {
					retType = fmt.Sprintf(" (%s) ", strings.Join(results, ", "))
				} else if len(results) == 1 {
					retType = " " + results[0] + " "
				}
			}

			fmt.Fprintf(&sb, "func (p *%s_ReverseProxy) %s(%s)%s{\n", name, methodName, strings.Join(paramsList, ", "), retType)
			fmt.Fprintf(&sb, "\targs := make([]*runtime.Var, %d)\n", len(paramNames))
			for i, pn := range paramNames {
				fmt.Fprintf(&sb, "\targs[%d] = p.program.ToVar(p.ctx, %s, p.bridge)\n", i, pn)
			}
			fmt.Fprintf(&sb, "\tresVar, err := p.program.InvokeCallable(p.ctx, p.callable, \"%s\", args)\n", methodName)
			fmt.Fprintf(&sb, "\t_ = err\n")

			// 处理返回值
			if funcType.Results != nil {
				var retVars []string
				numResults := len(funcType.Results.List)
				if numResults > 1 {
					// 脚本返回 Tuple (Array)
					fmt.Fprintf(&sb, "\tvar elements []*runtime.Var\n")
					fmt.Fprintf(&sb, "\tif resVar != nil && resVar.VType == runtime.TypeArray {\n")
					fmt.Fprintf(&sb, "\t\tif arr, ok := resVar.Ref.(*runtime.VMArray); ok {\n")
					fmt.Fprintf(&sb, "\t\t\telements = arr.Data\n")
					fmt.Fprintf(&sb, "\t\t}\n")
					fmt.Fprintf(&sb, "\t}\n")
					for i, r := range funcType.Results.List {
						rType := typeToString(r.Type)
						goType := toGoType(rType)
						varName := fmt.Sprintf("ret%d", i)
						fmt.Fprintf(&sb, "\tvar %s %s = %s\n", varName, goType, zeroValue(goType))
						fmt.Fprintf(&sb, "\tif %d < len(elements) {\n", i)
						emitReverseRead(&sb, varName, rType, fmt.Sprintf("elements[%d]", i))
						fmt.Fprintf(&sb, "\t}\n")
						retVars = append(retVars, varName)
					}
				} else {
					// 单一返回值
					r := funcType.Results.List[0]
					rType := typeToString(r.Type)
					goType := toGoType(rType)
					varName := "ret0"
					fmt.Fprintf(&sb, "\tvar %s %s = %s\n", varName, goType, zeroValue(goType))
					emitReverseRead(&sb, varName, rType, "resVar")
					retVars = append(retVars, varName)
				}
				fmt.Fprintf(&sb, "\treturn %s\n", strings.Join(retVars, ", "))
			} else {
				fmt.Fprintf(&sb, "\treturn\n")
			}
			fmt.Fprintf(&sb, "}\n\n")
		}
	}

	return sb.String()
}

func getSpec(funcType *ast.FuncType) string {
	var params []string
	if funcType.Params != nil {
		for i, p := range funcType.Params.List {
			pType := toVMType(p.Type)
			if i == 0 && (pType == "Context" || strings.HasSuffix(pType, ".Context")) {
				continue
			}
			prefix := ""
			if _, ok := p.Type.(*ast.Ellipsis); ok {
				prefix = "..."
				if strings.HasPrefix(pType, "Array<") && strings.HasSuffix(pType, ">") {
					pType = pType[6 : len(pType)-1]
				}
			}
			if len(p.Names) == 0 {
				params = append(params, prefix+pType)
			} else {
				for range p.Names {
					params = append(params, prefix+pType)
				}
			}
		}
	}
	var results []string
	if funcType.Results != nil {
		for _, r := range funcType.Results.List {
			t := toVMType(r.Type)
			if t == "error" {
				results = append(results, "Error")
			} else {
				results = append(results, t)
			}
		}
	}

	actualRet := "Void"
	if len(results) > 1 {
		actualRet = "tuple(" + strings.Join(results, ", ") + ")"
	} else if len(results) == 1 {
		actualRet = results[0]
	}

	return fmt.Sprintf("function(%s) %s", strings.Join(params, ", "), actualRet)
}

func toVMType(expr ast.Expr) string {
	switch t := expr.(type) {
	case *ast.Ident:
		name := t.Name
		if name == "byte" || name == "uint8" {
			return "Uint8"
		}
		if name == "int" || name == "int64" {
			return "Int64"
		}
		if name == "float64" || name == "float32" {
			return "Float64"
		}
		if name == "string" {
			return "String"
		}
		if name == "bool" {
			return "Bool"
		}
		if name == "any" || name == "interface{}" {
			return "Any"
		}
		return name
	case *ast.ArrayType:
		if ident, ok := t.Elt.(*ast.Ident); ok && (ident.Name == "byte" || ident.Name == "uint8") {
			return "TypeBytes"
		}
		return fmt.Sprintf("Array<%s>", toVMType(t.Elt))
	case *ast.MapType:
		return fmt.Sprintf("Map<%s, %s>", toVMType(t.Key), toVMType(t.Value))
	case *ast.StarExpr:
		return fmt.Sprintf("Ptr<%s>", toVMType(t.X))
	case *ast.Ellipsis:
		return fmt.Sprintf("Array<%s>", toVMType(t.Elt))
	case *ast.SelectorExpr:
		return t.Sel.Name
	default:
		return "Any"
	}
}

func typeToString(expr ast.Expr) string {
	switch t := expr.(type) {
	case *ast.Ident:
		return t.Name
	case *ast.ArrayType:
		return fmt.Sprintf("Array<%s>", typeToString(t.Elt))
	case *ast.MapType:
		return fmt.Sprintf("Map<%s, %s>", typeToString(t.Key), typeToString(t.Value))
	case *ast.StarExpr:
		return fmt.Sprintf("Ptr<%s>", typeToString(t.X))
	case *ast.SelectorExpr:
		return t.Sel.Name
	case *ast.InterfaceType:
		if t.Methods == nil || len(t.Methods.List) == 0 {
			return "Any"
		}
		return "interface{}"
	case *ast.Ellipsis:
		return fmt.Sprintf("Array<%s>", typeToString(t.Elt))
	default:
		return "Any"
	}
}

func emitReverseRead(sb *strings.Builder, varName, pType, sourceName string) {
	goType := toGoType(pType)
	if pType == "error" {
		fmt.Fprintf(sb, "\t\tif %s != nil {\n", sourceName)
		fmt.Fprintf(sb, "\t\t\tif raw := %s.Interface(); raw != nil {\n", sourceName)
		fmt.Fprintf(sb, "\t\t\t\tif s, ok := raw.(string); ok && s != \"\" {\n")
		fmt.Fprintf(sb, "\t\t\t\t\t%s = fmt.Errorf(\"%%s\", s)\n", varName)
		fmt.Fprintf(sb, "\t\t\t\t}\n")
		fmt.Fprintf(sb, "\t\t\t}\n")
		fmt.Fprintf(sb, "\t\t}\n")
		return
	}
	if pType == "Any" || pType == "any" {
		fmt.Fprintf(sb, "\t\tif %s != nil {\n", sourceName)
		fmt.Fprintf(sb, "\t\t\t%s = %s.Interface()\n", varName, sourceName)
		fmt.Fprintf(sb, "\t\t}\n")
		return
	}

	fmt.Fprintf(sb, "\t\tif %s != nil {\n", sourceName)
	fmt.Fprintf(sb, "\t\t\tif raw := %s.Interface(); raw != nil {\n", sourceName)
	// 基础类型直接断言
	switch pType {
	case "Int64", "int64", "Int", "int":
		fmt.Fprintf(sb, "\t\t\t\tswitch v := raw.(type) {\n\t\t\t\tcase int64: %s = %s(v)\n\t\t\t\tcase float64: %s = %s(v)\n\t\t\t\t}\n", varName, goType, varName, goType)
	case "String", "string":
		fmt.Fprintf(sb, "\t\t\t\tif v, ok := raw.(string); ok { %s = v }\n", varName)
	case "Bool", "bool":
		fmt.Fprintf(sb, "\t\t\t\tif v, ok := raw.(bool); ok { %s = v }\n", varName)
	case "Float64", "float64":
		fmt.Fprintf(sb, "\t\t\t\tif v, ok := raw.(float64); ok { %s = v }\n", varName)
	default:
		// 复杂类型（Struct/Map/Array）需要更精细的处理
		// 暂时尝试直接断言
		fmt.Fprintf(sb, "\t\t\t\tif v, ok := raw.(%s); ok { %s = v }\n", goType, varName)
	}
	fmt.Fprintf(sb, "\t\t\t}\n")
	fmt.Fprintf(sb, "\t\t}\n")
}

func zeroValue(t string) string {
	if strings.HasPrefix(t, "Ptr<") || strings.HasPrefix(t, "Array<") || t == "Any" || t == "any" || strings.HasPrefix(t, "*") || strings.HasPrefix(t, "[]") || t == "error" {
		return "nil"
	}
	switch t {
	case "Uint32", "uint32", "Int", "int", "Int64", "int64", "Int32", "int32":
		return "0"
	case "String", "string":
		return "\"\""
	case "Bool", "bool":
		return "false"
	case "Float64", "float64":
		return "0.0"
	case "TypeBytes":
		return "nil"
	default:
		return t + "{}"
	}
}
