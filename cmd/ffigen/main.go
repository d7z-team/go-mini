package main

import (
	"flag"
	"fmt"
	"go/ast"
	"go/parser"
	"go/token"
	"os"
	"strings"
)

var (
	pkgName = flag.String("pkg", "", "package name")
	outFile = flag.String("out", "", "output file")
)

func main() {
	flag.Parse()
	if *pkgName == "" || *outFile == "" {
		fmt.Println("Usage: ffigen -pkg <name> -out <file> [input files...]")
		os.Exit(1)
	}

	fset := token.NewFileSet()
	var interfaces []string
	var ifaceSpecs []*ast.TypeSpec
	structs := make(map[string]*ast.StructType)

	for _, arg := range flag.Args() {
		node, err := parser.ParseFile(fset, arg, nil, parser.ParseComments)
		if err != nil {
			panic(err)
		}

		ast.Inspect(node, func(n ast.Node) bool {
			if typeSpec, ok := n.(*ast.TypeSpec); ok {
				if _, ok := typeSpec.Type.(*ast.InterfaceType); ok {
					ifaceSpecs = append(ifaceSpecs, typeSpec)
				} else if str, ok := typeSpec.Type.(*ast.StructType); ok {
					structs[typeSpec.Name.Name] = str
				}
			}
			return true
		})
	}

	for _, spec := range ifaceSpecs {
		interfaces = append(interfaces, generateCode(*pkgName, spec.Name.Name, spec.Type.(*ast.InterfaceType), structs))
	}

	out := fmt.Sprintf("// Code generated by ffigen. DO NOT EDIT.\npackage %s\n\nimport (\n\t\"fmt\"\n\t\"gopkg.d7z.net/go-mini/core/ffigo\"\n)\n\n", *pkgName)
	out += strings.Join(interfaces, "\n")

	err := os.WriteFile(*outFile, []byte(out), 0o644)
	if err != nil {
		panic(err)
	}
}

func emitWrite(sb *strings.Builder, prefix string, pType string, structs map[string]*ast.StructType, bufName string, isHost bool) {
	if strings.HasPrefix(pType, "*") {
		if isHost {
			sb.WriteString(fmt.Sprintf("\t\t%s.WriteUint32(registry.Register(%s))\n", bufName, prefix))
		} else {
			sb.WriteString(fmt.Sprintf("\t\tif p.registry != nil {\n"))
			sb.WriteString(fmt.Sprintf("\t\t\t%s.WriteUint32(p.registry.Register(%s))\n", bufName, prefix))
			sb.WriteString(fmt.Sprintf("\t\t} else {\n"))
			sb.WriteString(fmt.Sprintf("\t\t\t%s.WriteUint32(0)\n", bufName))
			sb.WriteString(fmt.Sprintf("\t\t}\n"))
		}
		return
	}
	switch pType {
	case "uint32":
		sb.WriteString(fmt.Sprintf("\t%s.WriteUint32(%s)\n", bufName, prefix))
	case "int":
		sb.WriteString(fmt.Sprintf("\t%s.WriteInt64(int64(%s))\n", bufName, prefix))
	case "string":
		sb.WriteString(fmt.Sprintf("\t%s.WriteString(%s)\n", bufName, prefix))
	case "[]byte":
		sb.WriteString(fmt.Sprintf("\t%s.WriteBytes(%s)\n", bufName, prefix))
	case "any":
		sb.WriteString(fmt.Sprintf("\t%s.WriteAny(%s)\n", bufName, prefix))
	case "[]any", "...any":
		sb.WriteString(fmt.Sprintf("\t%s.WriteUint32(uint32(len(%s)))\n", bufName, prefix))
		sb.WriteString(fmt.Sprintf("\tfor _, item := range %s {\n", prefix))
		sb.WriteString(fmt.Sprintf("\t\t%s.WriteAny(item)\n", bufName))
		sb.WriteString(fmt.Sprintf("\t}\n"))
	default:
		if str, ok := structs[pType]; ok {
			for _, field := range str.Fields.List {
				for _, fName := range field.Names {
					fType := typeToString(field.Type)
					emitWrite(sb, prefix+"."+fName.Name, fType, structs, bufName, isHost)
				}
			}
		} else {
			sb.WriteString(fmt.Sprintf("\t// Unknown type for write: %s\n", pType))
		}
	}
}

func emitReadAssign(sb *strings.Builder, varName string, pType string, structs map[string]*ast.StructType, readerName string, isHost bool) {
	if strings.HasPrefix(pType, "*") {
		if isHost {
			sb.WriteString(fmt.Sprintf("\t\tif id := %s.ReadUint32(); id != 0 {\n", readerName))
			sb.WriteString(fmt.Sprintf("\t\t\tif obj, ok := registry.Get(id); ok {\n"))
			sb.WriteString(fmt.Sprintf("\t\t\t\t%s = obj.(%s)\n", varName, pType))
			sb.WriteString(fmt.Sprintf("\t\t\t} else {\n"))
			sb.WriteString(fmt.Sprintf("\t\t\t\treturn nil, fmt.Errorf(\"invalid handle ID: %%d\", id)\n"))
			sb.WriteString(fmt.Sprintf("\t\t\t}\n"))
			sb.WriteString(fmt.Sprintf("\t\t}\n"))
		} else {
			sb.WriteString(fmt.Sprintf("\t\tif id := %s.ReadUint32(); id != 0 {\n", readerName))
			sb.WriteString(fmt.Sprintf("\t\t\tif p.registry != nil {\n"))
			sb.WriteString(fmt.Sprintf("\t\t\t\tif obj, ok := p.registry.Get(id); ok {\n"))
			sb.WriteString(fmt.Sprintf("\t\t\t\t\t%s = obj.(%s)\n", varName, pType))
			sb.WriteString(fmt.Sprintf("\t\t\t\t}\n"))
			sb.WriteString(fmt.Sprintf("\t\t\t}\n"))
			sb.WriteString(fmt.Sprintf("\t\t}\n"))
		}
		return
	}
	switch pType {
	case "uint32":
		sb.WriteString(fmt.Sprintf("\t%s = %s.ReadUint32()\n", varName, readerName))
	case "int":
		sb.WriteString(fmt.Sprintf("\t%s = int(%s.ReadInt64())\n", varName, readerName))
	case "string":
		sb.WriteString(fmt.Sprintf("\t%s = %s.ReadString()\n", varName, readerName))
	case "[]byte":
		sb.WriteString(fmt.Sprintf("\t%s = %s.ReadBytes()\n", varName, readerName))
	case "any":
		if isHost {
			sb.WriteString(fmt.Sprintf("\t%s_raw := %s.ReadAny()\n", varName, readerName))
			sb.WriteString(fmt.Sprintf("\tif id, ok := %s_raw.(uint32); ok {\n", varName))
			sb.WriteString(fmt.Sprintf("\t\tif obj, ok := registry.Get(id); ok {\n"))
			sb.WriteString(fmt.Sprintf("\t\t\t%s = obj\n", varName))
			sb.WriteString(fmt.Sprintf("\t\t} else {\n"))
			sb.WriteString(fmt.Sprintf("\t\t\t%s = %s_raw\n", varName, varName))
			sb.WriteString(fmt.Sprintf("\t\t}\n"))
			sb.WriteString(fmt.Sprintf("\t} else {\n"))
			sb.WriteString(fmt.Sprintf("\t\t%s = %s_raw\n", varName, varName))
			sb.WriteString(fmt.Sprintf("\t}\n"))
		} else {
			sb.WriteString(fmt.Sprintf("\t%s = %s.ReadAny()\n", varName, readerName))
		}
	case "[]any", "...any":
		sb.WriteString(fmt.Sprintf("\tl_%s := int(%s.ReadUint32())\n", varName, readerName))
		sb.WriteString(fmt.Sprintf("\t%s = make([]any, l_%s)\n", varName, varName))
		sb.WriteString(fmt.Sprintf("\tfor i_%s := 0; i_%s < l_%s; i_%s++ {\n", varName, varName, varName, varName))
		sb.WriteString(fmt.Sprintf("\t\t%s[i_%s] = %s.ReadAny()\n", varName, varName, readerName))
		sb.WriteString(fmt.Sprintf("\t}\n"))
	default:
		if str, ok := structs[pType]; ok {
			for _, field := range str.Fields.List {
				for _, fName := range field.Names {
					fType := typeToString(field.Type)
					emitReadAssign(sb, varName+"."+fName.Name, fType, structs, readerName, isHost)
				}
			}
		} else {
			sb.WriteString(fmt.Sprintf("\t// Unknown type for read: %s\n", pType))
		}
	}
}

func generateCode(pkg, name string, iface *ast.InterfaceType, structs map[string]*ast.StructType) string {
	var sb strings.Builder

	// Generate Method IDs
	sb.WriteString(fmt.Sprintf("const (\n"))
	for i, method := range iface.Methods.List {
		if len(method.Names) == 0 {
			continue
		}
		methodName := method.Names[0].Name
		sb.WriteString(fmt.Sprintf("\tMethodID_%s_%s = %d\n", name, methodName, i+1))
	}
	sb.WriteString(")\n\n")

	// Generate VM Proxy
	sb.WriteString(fmt.Sprintf("type %sProxy struct {\n\tbridge ffigo.FFIBridge\n\tregistry *ffigo.HandleRegistry\n}\n\n", name))

	for _, method := range iface.Methods.List {
		if len(method.Names) == 0 {
			continue
		}
		methodName := method.Names[0].Name
		funcType := method.Type.(*ast.FuncType)

		sb.WriteString(fmt.Sprintf("func (p *%sProxy) %s(", name, methodName))

		// Params
		var paramNames []string
		if funcType.Params != nil {
			for j, param := range funcType.Params.List {
				for _, pName := range param.Names {
					pType := typeToString(param.Type)
					sb.WriteString(fmt.Sprintf("%s %s", pName.Name, pType))
					if j < len(funcType.Params.List)-1 {
						sb.WriteString(", ")
					}
					paramNames = append(paramNames, pName.Name)
				}
			}
		}
		sb.WriteString(") ")

		// Results
		var hasErr bool
		if funcType.Results != nil {
			sb.WriteString("(")
			for j, result := range funcType.Results.List {
				rType := typeToString(result.Type)
				if rType == "error" {
					hasErr = true
				}
				sb.WriteString(rType)
				if j < len(funcType.Results.List)-1 {
					sb.WriteString(", ")
				}
			}
			sb.WriteString(") ")
		}

		sb.WriteString("{\n")
		sb.WriteString("\tbuf := ffigo.GetBuffer()\n")
		sb.WriteString("\tdefer ffigo.ReleaseBuffer(buf)\n\n")

		// Serialize args
		if funcType.Params != nil {
			for _, param := range funcType.Params.List {
				for _, pName := range param.Names {
					pType := typeToString(param.Type)
					emitWrite(&sb, pName.Name, pType, structs, "buf", false)
				}
			}
		}

		// Deserialize results preparation
		var retStmt []string
		needsRetBuf := false
		if funcType.Results != nil {
			for _, result := range funcType.Results.List {
				rType := typeToString(result.Type)
				if rType != "error" {
					needsRetBuf = true
				}
			}
		}

		if needsRetBuf {
			sb.WriteString(fmt.Sprintf("\n\tretData, err := p.bridge.Call(MethodID_%s_%s, buf.Bytes())\n", name, methodName))
		} else {
			sb.WriteString(fmt.Sprintf("\n\t_, err := p.bridge.Call(MethodID_%s_%s, buf.Bytes())\n", name, methodName))
		}

		if hasErr {
			sb.WriteString("\tif err != nil {\n\t\treturn ")
			// Return zero values + err
			if funcType.Results != nil {
				for j, result := range funcType.Results.List {
					rType := typeToString(result.Type)
					if rType == "error" {
						sb.WriteString("err")
					} else {
						sb.WriteString(zeroValue(rType))
						if j < len(funcType.Results.List)-1 {
							sb.WriteString(", ")
						}
					}
				}
			}
			sb.WriteString("\n\t}\n\n")
		} else {
			sb.WriteString("\t_ = err\n")
		}

		if needsRetBuf {
			sb.WriteString("\tretBuf := ffigo.NewReader(retData)\n")
			if hasErr {
				sb.WriteString("\tstatus := retBuf.ReadByte()\n")
				sb.WriteString("\tif status != 0 {\n")
				sb.WriteString("\t\terrMsg := retBuf.ReadString()\n")
				sb.WriteString("\t\treturn ")
				if funcType.Results != nil {
					for j, result := range funcType.Results.List {
						rType := typeToString(result.Type)
						if rType == "error" {
							sb.WriteString("fmt.Errorf(\"%s\", errMsg)")
						} else {
							sb.WriteString(zeroValue(rType))
							if j < len(funcType.Results.List)-1 {
								sb.WriteString(", ")
							}
						}
					}
				}
				sb.WriteString("\n\t}\n")
			}
		}

		if funcType.Results != nil {
			for i, result := range funcType.Results.List {
				rType := typeToString(result.Type)
				if rType == "error" {
					retStmt = append(retStmt, "nil")
					continue
				}

				varName := fmt.Sprintf("v_%d", i)
				sb.WriteString(fmt.Sprintf("\tvar %s %s\n", varName, rType))
				emitReadAssign(&sb, varName, rType, structs, "retBuf", false)
				retStmt = append(retStmt, varName)
			}
		}

		if len(retStmt) > 0 {
			sb.WriteString(fmt.Sprintf("\treturn %s\n", strings.Join(retStmt, ", ")))
		}

		sb.WriteString("}\n\n")
	}

	// Generate Host Router
	sb.WriteString(fmt.Sprintf("func %sHostRouter(impl %s, registry *ffigo.HandleRegistry, methodID uint32, args []byte) ([]byte, error) {\n", name, name))
	sb.WriteString("\treqBuf := ffigo.NewReader(args)\n")
	sb.WriteString("\tswitch methodID {\n")

	for _, method := range iface.Methods.List {
		if len(method.Names) == 0 {
			continue
		}
		methodName := method.Names[0].Name
		funcType := method.Type.(*ast.FuncType)

		sb.WriteString(fmt.Sprintf("\tcase MethodID_%s_%s:\n", name, methodName))

		var paramVars []string
		if funcType.Params != nil {
			for _, param := range funcType.Params.List {
				for _, pName := range param.Names {
					pType := typeToString(param.Type)
					declType := pType
					isVariadic := strings.HasPrefix(pType, "...")
					if isVariadic {
						declType = "[]" + strings.TrimPrefix(pType, "...")
					}
					sb.WriteString(fmt.Sprintf("\t\tvar %s %s\n", pName.Name, declType))
					emitReadAssign(&sb, pName.Name, pType, structs, "reqBuf", true)
					if isVariadic {
						paramVars = append(paramVars, pName.Name+"...")
					} else {
						paramVars = append(paramVars, pName.Name)
					}
				}
			}
		}

		callStmt := fmt.Sprintf("impl.%s(%s)", methodName, strings.Join(paramVars, ", "))

		var retVars []string
		var hasErr bool
		if funcType.Results != nil {
			for i, result := range funcType.Results.List {
				rType := typeToString(result.Type)
				if rType == "error" {
					hasErr = true
					retVars = append(retVars, "err")
				} else {
					retVars = append(retVars, fmt.Sprintf("r%d", i))
				}
			}
			sb.WriteString(fmt.Sprintf("\t\t%s := %s\n", strings.Join(retVars, ", "), callStmt))
		} else {
			sb.WriteString(fmt.Sprintf("\t\t%s\n", callStmt))
		}

		sb.WriteString("\t\tresBuf := ffigo.GetBuffer()\n")

		if hasErr {
			sb.WriteString("\t\tif err != nil {\n")
			sb.WriteString("\t\t\tresBuf.WriteByte(1)\n")
			sb.WriteString("\t\t\tresBuf.WriteString(err.Error())\n")
			sb.WriteString("\t\t} else {\n")
			sb.WriteString("\t\t\tresBuf.WriteByte(0)\n")
			for i, result := range funcType.Results.List {
				rType := typeToString(result.Type)
				if rType != "error" {
					emitWrite(&sb, fmt.Sprintf("r%d", i), rType, structs, "resBuf", true)
					break // Only supports single value in Result<T>
				}
			}
			sb.WriteString("\t\t}\n")
			sb.WriteString("\t\treturn resBuf.Bytes(), nil\n")
		} else {
			if funcType.Results != nil {
				for i, result := range funcType.Results.List {
					rType := typeToString(result.Type)
					emitWrite(&sb, fmt.Sprintf("r%d", i), rType, structs, "resBuf", true)
				}
			}
			sb.WriteString("\t\treturn resBuf.Bytes(), nil\n")
		}
	}

	sb.WriteString("\tdefault:\n\t\treturn nil, fmt.Errorf(\"unknown method ID %d\", methodID)\n")
	sb.WriteString("\t}\n}\n")

	return sb.String()
}

func typeToString(expr ast.Expr) string {
	switch t := expr.(type) {
	case *ast.Ident:
		return t.Name
	case *ast.ArrayType:
		return "[]" + typeToString(t.Elt)
	case *ast.StarExpr:
		return "*" + typeToString(t.X)
	case *ast.SelectorExpr:
		return typeToString(t.X) + "." + t.Sel.Name
	case *ast.InterfaceType:
		if t.Methods == nil || len(t.Methods.List) == 0 {
			return "any"
		}
		return "interface{}"
	case *ast.Ellipsis:
		if t.Elt == nil {
			return "...any" // Just in case
		}
		return "..." + typeToString(t.Elt)
	default:
		return "unknown"
	}
}

func zeroValue(t string) string {
	if strings.HasPrefix(t, "*") || strings.HasPrefix(t, "[]") || strings.HasPrefix(t, "...") || t == "any" {
		return "nil"
	}
	switch t {
	case "uint32", "int", "int64", "float64":
		return "0"
	case "string":
		return "\"\""
	case "bool":
		return "false"
	default:
		return t + "{}"
	}
}
