func collectImportAliases(files []*ast.File) map[string]string {
	res := make(map[string]string)
	for _, node := range files {
		for _, imp := range node.Imports {
			path := strings.Trim(imp.Path.Value, "\"")
			parts := strings.Split(path, "/")
			alias := parts[len(parts)-1]
			if imp.Name != nil {
				alias = imp.Name.Name
			}
			res[alias] = path
		}
	}
	return res
}

func isIgnorableTypeCheckError(err error, imports map[string]string) bool {
	if err == nil {
		return true
	}
	var list scanner.ErrorList
	if errors.As(err, &list) {
		if len(list) == 0 {
			return true
		}
		for _, item := range list {
			if !isIgnorableTypeCheckMessage(item.Msg, imports) {
				return false
			}
		}
		return true
	}
	return isIgnorableTypeCheckMessage(err.Error(), imports)
}

func isIgnorableTypeCheckMessage(msg string, imports map[string]string) bool {
	msg = strings.TrimSpace(msg)
	needle := "undefined: "
	idx := strings.LastIndex(msg, needle)
	if idx == -1 {
		return false
	}
	alias := strings.TrimSpace(strings.TrimPrefix(msg[idx:], needle))
	if alias == "" {
		return false
	}
	_, ok := imports[alias]
	return ok
}

func generatePackageOutput(outputPath string, data packageData) (err error) {
	defer func() {
		if r := recover(); r != nil {
			err = fmt.Errorf("ffigen generation failed: %v", r)
		}
	}()
	schemas := newSchemaRegistry()

	var generated []string
	for _, target := range data.targets {
		generated = append(generated, generateCode(target.spec, data.structs, data.interfaces, target.meta, data.constants, schemas, data.ownedStructs))
	}

	var schemaDefs strings.Builder
	if schemas != nil {
		for _, decl := range schemas.ordered {
			fmt.Fprintf(&schemaDefs, "var %s = runtime.MustParseRuntimeStructSpec(\"%s\", runtime.%s, \"%s\")\n\n", decl.varName, decl.displayName, decl.ownership, decl.specLiteral)
		}
	}

	var sb strings.Builder
	sb.WriteString("// Code generated by ffigen. DO NOT EDIT.\n")
	fmt.Fprintf(&sb, "package %s\n\n", *pkgName)
	sb.WriteString("import (\n")
	stdPackages := map[string]string{
		"context": "context",
		"fmt":     "fmt",
		"strings": "strings",
		"ffigo":   "gopkg.d7z.net/go-mini/core/ffigo",
		"runtime": "gopkg.d7z.net/go-mini/core/runtime",
	}
	for _, path := range stdPackages {
		fmt.Fprintf(&sb, "\t\"%s\"\n", path)
	}
	for alias, path := range knownImports {
		if _, ok := stdPackages[alias]; ok {
			continue
		}
		if alias == path[strings.LastIndex(path, "/")+1:] {
			fmt.Fprintf(&sb, "\t\"%s\"\n", path)
		} else {
			fmt.Fprintf(&sb, "\t%s \"%s\"\n", alias, path)
		}
	}
	sb.WriteString(")\n\n")
	sb.WriteString(schemaDefs.String())
	sb.WriteString(strings.Join(generated, "\n"))
	return writeFormattedSource(outputPath, sb.String())
}

func writeFormattedSource(outputPath, source string) error {
	fsetOut := token.NewFileSet()
	fOut, err := parser.ParseFile(fsetOut, "", source, parser.ParseComments)
	if err != nil {
		_ = os.WriteFile(outputPath, []byte(source), 0o644)
		return fmt.Errorf("parsing generated code for import cleanup in %s: %w\n%s", outputPath, err, parserErrorContext(err, source))
	}
	usedAliases := make(map[string]bool)
	ast.Inspect(fOut, func(n ast.Node) bool {
		switch node := n.(type) {
		case *ast.SelectorExpr:
			if id, ok := node.X.(*ast.Ident); ok {
				usedAliases[id.Name] = true
			}
		case *ast.Field:
			if node.Type != nil {
				ast.Inspect(node.Type, func(tn ast.Node) bool {
					if se, ok := tn.(*ast.SelectorExpr); ok {
						if id, ok := se.X.(*ast.Ident); ok {
							usedAliases[id.Name] = true
						}
					}
					return true
				})
			}
		}
		return true
	})
	var stdSpecs []ast.Spec
	var extSpecs []ast.Spec
	var otherDecls []ast.Decl
	for _, decl := range fOut.Decls {
		if gd, ok := decl.(*ast.GenDecl); ok && gd.Tok == token.IMPORT {
			for _, spec := range gd.Specs {
				is := spec.(*ast.ImportSpec)
				path := strings.Trim(is.Path.Value, "\"")
				alias := path[strings.LastIndex(path, "/")+1:]
				if is.Name != nil {
					alias = is.Name.Name
				}
				if !usedAliases[alias] {
					continue
				}
				if is.Name != nil {
					is.Name.NamePos = token.NoPos
				}
				is.Path.ValuePos = token.NoPos
				is.EndPos = token.NoPos
				if !strings.Contains(path, ".") {
					stdSpecs = append(stdSpecs, is)
				} else {
					extSpecs = append(extSpecs, is)
				}
			}
		} else {
			otherDecls = append(otherDecls, decl)
		}
	}
	sortSpecs := func(specs []ast.Spec) {
		sort.Slice(specs, func(i, j int) bool {
			return strings.Trim(specs[i].(*ast.ImportSpec).Path.Value, "\"") < strings.Trim(specs[j].(*ast.ImportSpec).Path.Value, "\"")
		})
	}
	sortSpecs(stdSpecs)
	sortSpecs(extSpecs)
	var newDecls []ast.Decl
	if len(stdSpecs) > 0 {
		newDecls = append(newDecls, &ast.GenDecl{Tok: token.IMPORT, Specs: stdSpecs, Lparen: 1})
	}
	if len(extSpecs) > 0 {
		newDecls = append(newDecls, &ast.GenDecl{Tok: token.IMPORT, Specs: extSpecs, Lparen: 1})
	}
	newDecls = append(newDecls, otherDecls...)
	fOut.Decls = newDecls
	var finalBuf bytes.Buffer
	if err := format.Node(&finalBuf, fsetOut, fOut); err != nil {
		_ = os.WriteFile(outputPath, []byte(source), 0o644)
		return fmt.Errorf("formatting generated code: %w", err)
	}
	return os.WriteFile(outputPath, finalBuf.Bytes(), 0o644)
}
