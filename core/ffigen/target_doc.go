package ffigen

import (
	"go/ast"
	"go/token"
	"path/filepath"
	"strings"
)

func (g *Generator) resolveTargetDoc(file *ast.File, genDecl *ast.GenDecl, typeSpec *ast.TypeSpec) *ast.CommentGroup {
	return resolveTargetDocWithFileSet(g.fset, file, genDecl, typeSpec)
}

func isGeneratedFile(filename string, file *ast.File) bool {
	if isGeneratedFilename(filename) {
		return true
	}
	if file == nil {
		return false
	}
	for _, group := range file.Comments {
		for _, comment := range group.List {
			if strings.Contains(comment.Text, "Code generated by ffigen. DO NOT EDIT.") {
				return true
			}
		}
	}
	return false
}

func isGeneratedFilename(filename string) bool {
	lower := strings.ToLower(filepath.Base(filename))
	return strings.HasSuffix(lower, ".go") && (strings.HasPrefix(lower, "ffigen_") || strings.HasSuffix(lower, "_ffigen.go"))
}

func resolveTargetDocWithFileSet(fileSet *token.FileSet, file *ast.File, genDecl *ast.GenDecl, typeSpec *ast.TypeSpec) *ast.CommentGroup {
	if typeSpec.Doc != nil {
		return typeSpec.Doc
	}
	if genDecl.Doc != nil {
		return genDecl.Doc
	}
	specLine := fileSet.Position(typeSpec.Pos()).Line
	var best *ast.CommentGroup
	bestLine := -1
	for _, group := range file.Comments {
		endLine := fileSet.Position(group.End()).Line
		if endLine >= specLine || specLine-endLine > 2 {
			continue
		}
		if endLine > bestLine {
			best = group
			bestLine = endLine
		}
	}
	return best
}
